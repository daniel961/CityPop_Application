package com.citypop.hit.citypop;

public class cityUpdateObject {

    String author;
    String city;
    String date;
    String imageUrl;
    String text;
    String subject;
    String time;

    public cityUpdateObject(String author, String city, String date,
                            String imageUrl, String text, String subject, String time) {
        this.author = author;
        this.city = city;
        this.date = date;
        this.imageUrl = imageUrl;
        this.text = text;
        this.subject = subject;
        this.time = time;
    }

}
